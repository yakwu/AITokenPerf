// ---- Helpers ----
function formatInterval(seconds) {
  const s = parseInt(seconds) || 0;
  if (s < 60) return s + ' 秒';
  if (s < 3600) return (s / 60) + ' 分钟';
  return (s / 3600) + ' 小时';
}

function formatTime(iso) {
  if (!iso) return '-';
  try {
    // PostgreSQL timestamptz 已带时区（如 +00），不要重复拼 Z
    const d = new Date(iso.endsWith('Z') || iso.includes('+') ? iso : iso + 'Z');
    return d.toLocaleString('zh-CN', { month: '2-digit', day: '2-digit', hour: '2-digit', minute: '2-digit' });
  } catch {
    return iso;
  }
}

function intervalSuffix(seconds) {
  const s = parseInt(seconds) || 300;
  if (s % 86400 === 0) return (s / 86400) + 'd';
  if (s % 3600 === 0) return (s / 3600) + 'h';
  if (s % 60 === 0) return (s / 60) + 'm';
  return s + 's';
}

function autoGenerateName() {
  if (!nameAutoGenerated.value) return;
  const f = createForm.value;
  const profileName = f.profile_ids[0] || '';
  if (!profileName) { f.name = ''; return; }
  const suffix = intervalSuffix(f.schedule_value);
  let base = `${profileName}-${suffix}`;
  const existingNames = schedules.value.map(s => s.name);
  if (!existingNames.includes(base)) { f.name = base; return; }
  let i = 2;
  while (existingNames.includes(`${base}-${i}`)) i++;
  f.name = `${base}-${i}`;
}

watch([() => createForm.value.profile_ids, () => createForm.value.schedule_value], () => autoGenerateName(), { deep: true });

function destroyCharts() {
  if (trendLatencyChart) { trendLatencyChart.destroy(); trendLatencyChart = null; }
  if (trendQualityChart) { trendQualityChart.destroy(); trendQualityChart = null; }
}

// ---- Data Loading ----
async function refresh() {
  loading.value = true;
  try {
    const [schedData, profData] = await Promise.all([
      getSchedules(),
      getProfiles(),
    ]);
    schedules.value = schedData.schedules || [];
    profiles.value = (profData.profiles || []).map(p => p.name);
  } catch (e) {
    toast('加载失败: ' + e.message, 'error');
  }
  loading.value = false;
}

// ---- CRUD ----
async function createSchedule() {
  const f = createForm.value;
  if (!f.name.trim()) { toast('请输入任务名称', 'info'); return; }
  if (f.profile_ids.length === 0) { toast('请至少选择一个配置', 'info'); return; }
  createLoading.value = true;
  try {
    const res = await createScheduleApi({
      name: f.name.trim(),
      profile_ids: f.profile_ids,
      configs_json: {
        concurrency_levels: [Number(f.concurrency)],
        mode: f.mode,
        max_tokens: Number(f.max_tokens),
        timeout: Number(f.timeout),
        duration: Number(f.duration),
        system_prompt: f.system_prompt,
        user_prompt: f.user_prompt,
      },
      schedule_type: 'interval',
      schedule_value: String(f.schedule_value),
    });
    if (res.error) { toast(res.error, 'error'); return; }
    toast('定时任务已创建', 'success');
    showCreateForm.value = false;
    createForm.value = defaultForm();
    nameAutoGenerated.value = true;
    await refresh();
  } catch (e) {
    toast('创建失败: ' + e.message, 'error');
  }
  createLoading.value = false;
}

async function saveNewProfile() {
  const np = newProfile.value;
  if (!np.name.trim() || !np.base_url.trim() || !np.model.trim()) return;
  try {
    await api('/api/profiles/save', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        name: np.name.trim(),
        base_url: np.base_url.trim(),
        api_key: np.api_key.trim(),
        model: np.model.trim(),
        api_version: '2023-06-01',
      }),
    });
    toast('配置已保存', 'success');
    showNewProfile.value = false;
    newProfile.value = { name: '', base_url: '', api_key: '', model: '' };
    // 重新加载配置列表并自动选中新建的
    const profData = await getProfiles();
    profiles.value = (profData.profiles || []).map(p => p.name);
    const savedName = np.name.trim();
    if (!createForm.value.profile_ids.includes(savedName)) {
      createForm.value.profile_ids = [...createForm.value.profile_ids, savedName];
    }
  } catch (e) {
    toast('保存失败: ' + e.message, 'error');
  }
}

